#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	min_int;

	min_int = 1 << ((sizeof(int) * 8) - 1);
	if (n == min_int)
	{
		ft_putstr_fd("-2147483648", fd);
		return ;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = n * -1;
	}
	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	ft_putchar_fd((n % 10) + '0', fd);
}
